-- Supabase Schema for Invite Genie
-- Run this in your Supabase Dashboard -> SQL Editor

-- Enable UUID extension for primary keys
CREATE EXTENSION IF NOT EXISTS "uuid-ossp";

-- 1. Profiles Table
-- Stores user profiles, linked to Supabase Auth.
CREATE TABLE IF NOT EXISTS public.profiles (
  id uuid PRIMARY KEY REFERENCES auth.users(id) ON DELETE CASCADE,
  full_name text,
  email text UNIQUE,
  role text DEFAULT 'public_guest', -- e.g., 'public_guest', 'event_host', 'app_admin'
  account_type text DEFAULT 'user', -- e.g., 'user', 'vendor', 'freelancer'
  avatar_url text,
  phone text,
  created_at timestamptz DEFAULT now(),
  updated_at timestamptz DEFAULT now()
);

-- 2. Events Table
-- Stores event details created by hosts.
CREATE TABLE IF NOT EXISTS public.events (
  id uuid PRIMARY KEY DEFAULT uuid_generate_v4(),
  host_id uuid REFERENCES public.profiles(id) ON DELETE SET NULL,
  title text NOT NULL,
  description text,
  location text,
  event_date timestamptz,
  event_time text, -- e.g., "19:00"
  price numeric DEFAULT 0,
  total_tickets integer DEFAULT 100,
  available_tickets integer DEFAULT 100,
  tickets_sold integer DEFAULT 0,
  revenue numeric DEFAULT 0,
  status text DEFAULT 'active', -- e.g., 'active', 'draft', 'past', 'cancelled'
  category text, -- e.g., 'Wedding', 'Corporate', 'Concert'
  image_url text, -- Cover image for the event
  created_at timestamptz DEFAULT now(),
  updated_at timestamptz DEFAULT now()
);

-- 3. Tickets Table
-- Stores individual ticket records for events.
CREATE TABLE IF NOT EXISTS public.tickets (
  id uuid PRIMARY KEY DEFAULT uuid_generate_v4(),
  event_id uuid REFERENCES public.events(id) ON DELETE CASCADE,
  user_id uuid REFERENCES public.profiles(id) ON DELETE SET NULL,
  buyer_name text,
  buyer_email text,
  event_name text, -- Denormalized for easier access
  ticket_type text DEFAULT 'Standard', -- e.g., 'Standard', 'VIP', 'Table'
  price numeric DEFAULT 0,
  status text DEFAULT 'valid', -- e.g., 'valid', 'used', 'cancelled'
  qr_value text UNIQUE, -- Value encoded in QR code for check-in
  checked_in_at timestamptz,
  created_at timestamptz DEFAULT now(),
  updated_at timestamptz DEFAULT now()
);

-- 4. Payments Table
-- Records all payment transactions.
CREATE TABLE IF NOT EXISTS public.payments (
  id uuid PRIMARY KEY DEFAULT uuid_generate_v4(),
  event_id uuid REFERENCES public.events(id) ON DELETE SET NULL,
  user_id uuid REFERENCES public.profiles(id) ON DELETE SET NULL,
  ticket_id uuid REFERENCES public.tickets(id) ON DELETE SET NULL,
  amount numeric DEFAULT 0,
  currency text DEFAULT 'FCFA',
  method text, -- e.g., 'Mobile Money', 'Card', 'Cash'
  status text DEFAULT 'completed', -- e.g., 'completed', 'pending', 'failed', 'refunded'
  created_at timestamptz DEFAULT now(),
  updated_at timestamptz DEFAULT now()
);

-- 5. Invitations Table
-- Stores details about invitations sent.
CREATE TABLE IF NOT EXISTS public.invitations (
  id uuid PRIMARY KEY DEFAULT uuid_generate_v4(),
  event_id uuid REFERENCES public.events(id) ON DELETE CASCADE,
  host_id uuid REFERENCES public.profiles(id) ON DELETE SET NULL,
  guest_id uuid REFERENCES public.profiles(id) ON DELETE SET NULL, -- If guest is a registered user
  guest_email text,
  guest_name text,
  status text DEFAULT 'sent', -- e.g., 'sent', 'opened', 'viewed', 'rsvp_yes', 'rsvp_no'
  template_id text,
  invitation_url text,
  qr_code_url text,
  created_at timestamptz DEFAULT now(),
  updated_at timestamptz DEFAULT now()
);

-- 6. Invitation Guests Table (for external guests or Google Forms integration)
-- Stores guest details, especially those imported from external sources like Google Forms.
CREATE TABLE IF NOT EXISTS public.invitation_guests (
  id uuid PRIMARY KEY DEFAULT uuid_generate_v4(),
  event_id uuid REFERENCES public.events(id) ON DELETE CASCADE,
  unique_id text UNIQUE, -- Matches the UID generated by Apps Script or Genie
  guest_id text,         -- GUEST-0001 format (from Apps Script)
  name text,
  email text,
  phone text,
  status text DEFAULT 'Pending', -- e.g., 'Pending', 'Confirmed', 'CheckedIn'
  source text DEFAULT 'Genie', -- 'Genie' or 'GoogleForm'
  total_views integer DEFAULT 0,
  total_downloads integer DEFAULT 0,
  last_activity text, -- e.g., 'EMAIL_SENT 2023-01-01T12:00:00Z'
  created_at timestamptz DEFAULT now(),
  updated_at timestamptz DEFAULT now()
);

-- 7. Invitation Logs Table
-- Tracks activities related to invitations (sent, opened, viewed, downloaded).
CREATE TABLE IF NOT EXISTS public.invitation_logs (
  id uuid PRIMARY KEY DEFAULT uuid_generate_v4(),
  guest_id uuid REFERENCES public.invitation_guests(id) ON DELETE CASCADE,
  event_type text, -- 'email_sent', 'email_open', 'invitation_view', 'invitation_download', 'qr_checkin'
  payload jsonb, -- Additional data related to the log event
  created_at timestamptz DEFAULT now()
);

-- 8. Checkins Table
-- Records when a guest checks into an event.
CREATE TABLE IF NOT EXISTS public.checkins (
  id uuid PRIMARY KEY DEFAULT uuid_generate_v4(),
  guest_id uuid REFERENCES public.invitation_guests(id) ON DELETE CASCADE,
  event_id uuid REFERENCES public.events(id) ON DELETE CASCADE,
  device_id text,
  ip_address text,
  checked_in_at timestamptz DEFAULT now()
);

-- 9. Memories Table
-- User-generated content (photos/videos) from events.
CREATE TABLE IF NOT EXISTS public.memories (
  id uuid PRIMARY KEY DEFAULT uuid_generate_v4(),
  event_id uuid REFERENCES public.events(id) ON DELETE CASCADE,
  user_id uuid REFERENCES public.profiles(id) ON DELETE SET NULL,
  caption text,
  media_url text NOT NULL,
  likes integer DEFAULT 0,
  comments_count integer DEFAULT 0,
  created_at timestamptz DEFAULT now(),
  updated_at timestamptz DEFAULT now()
);

-- 10. Posts Table (for social feed)
CREATE TABLE IF NOT EXISTS public.posts (
  id uuid PRIMARY KEY DEFAULT uuid_generate_v4(),
  user_id uuid REFERENCES public.profiles(id) ON DELETE SET NULL,
  user_name text,
  content text,
  media_url text,
  event_id uuid REFERENCES public.events(id) ON DELETE SET NULL,
  post_type text DEFAULT 'post', -- 'post', 'memory', 'event', 'gig', 'share'
  likes_count integer DEFAULT 0,
  comments_count integer DEFAULT 0,
  shares_count integer DEFAULT 0,
  created_at timestamptz DEFAULT now(),
  updated_at timestamptz DEFAULT now()
);

-- 11. Comments Table (for posts)
CREATE TABLE IF NOT EXISTS public.comments (
  id uuid PRIMARY KEY DEFAULT uuid_generate_v4(),
  post_id uuid REFERENCES public.posts(id) ON DELETE CASCADE,
  user_id uuid REFERENCES public.profiles(id) ON DELETE SET NULL,
  user_name text,
  text text NOT NULL,
  created_at timestamptz DEFAULT now()
);

-- 12. Galleries Table (for event-specific media)
CREATE TABLE IF NOT EXISTS public.galleries (
  id uuid PRIMARY KEY DEFAULT uuid_generate_v4(),
  event_id uuid REFERENCES public.events(id) ON DELETE CASCADE,
  media_url text NOT NULL,
  caption text,
  uploaded_by uuid REFERENCES public.profiles(id) ON DELETE SET NULL,
  created_at timestamptz DEFAULT now()
);

-- 13. Vendors Table (for service providers)
CREATE TABLE IF NOT EXISTS public.vendors (
  id uuid PRIMARY KEY DEFAULT uuid_generate_v4(),
  user_id uuid REFERENCES public.profiles(id) ON DELETE SET NULL,
  name text NOT NULL,
  description text,
  category text, -- e.g., 'Caterer', 'Photographer', 'DJ'
  location text,
  contact_email text,
  contact_phone text,
  rating numeric DEFAULT 0,
  jobs_completed integer DEFAULT 0,
  is_verified boolean DEFAULT false,
  profile_image_url text,
  created_at timestamptz DEFAULT now(),
  updated_at timestamptz DEFAULT now()
);

-- 14. Freelancers Table (similar to vendors but for individual professionals)
CREATE TABLE IF NOT EXISTS public.freelancers (
  id uuid PRIMARY KEY DEFAULT uuid_generate_v4(),
  user_id uuid REFERENCES public.profiles(id) ON DELETE CASCADE,
  name text NOT NULL,
  category text, -- e.g., 'Photographer', 'Musician', 'Designer'
  bio text,
  location text,
  hourly_rate numeric DEFAULT 0,
  rating numeric DEFAULT 0,
  is_verified boolean DEFAULT false,
  is_available boolean DEFAULT true,
  profile_image_url text,
  created_at timestamptz DEFAULT now(),
  updated_at timestamptz DEFAULT now()
);

-- 15. Gigs Table (freelancer job postings)
CREATE TABLE IF NOT EXISTS public.gigs (
  id uuid PRIMARY KEY DEFAULT uuid_generate_v4(),
  host_id uuid REFERENCES public.profiles(id) ON DELETE SET NULL,
  event_id uuid REFERENCES public.events(id) ON DELETE SET NULL,
  title text NOT NULL,
  description text,
  budget numeric DEFAULT 0,
  status text DEFAULT 'open', -- 'open', 'assigned', 'completed', 'cancelled'
  created_at timestamptz DEFAULT now(),
  updated_at timestamptz DEFAULT now()
);

-- 16. Freelancer Bookings Table
CREATE TABLE IF NOT EXISTS public.freelancer_bookings (
  id uuid PRIMARY KEY DEFAULT uuid_generate_v4(),
  gig_id uuid REFERENCES public.gigs(id) ON DELETE CASCADE,
  freelancer_id uuid REFERENCES public.freelancers(id) ON DELETE CASCADE,
  user_id uuid REFERENCES public.profiles(id) ON DELETE SET NULL, -- The user who booked the freelancer
  status text DEFAULT 'pending', -- 'pending', 'confirmed', 'completed', 'cancelled'
  created_at timestamptz DEFAULT now(),
  updated_at timestamptz DEFAULT now()
);

-- 17. Seat Maps Table
-- Stores layout data for event seating.
CREATE TABLE IF NOT EXISTS public.seat_maps (
  id uuid PRIMARY KEY DEFAULT uuid_generate_v4(),
  event_id uuid REFERENCES public.events(id) ON DELETE CASCADE,
  map_data jsonb, -- JSON representation of the seat map layout
  created_at timestamptz DEFAULT now(),
  updated_at timestamptz DEFAULT now()
);

-- 18. Guest Assignments Table (for specific seat assignments)
CREATE TABLE IF NOT EXISTS public.guest_assignments (
  id uuid PRIMARY KEY DEFAULT uuid_generate_v4(),
  event_id uuid REFERENCES public.events(id) ON DELETE CASCADE,
  ticket_id uuid REFERENCES public.tickets(id) ON DELETE CASCADE,
  seat_id text, -- Identifier for the seat (e.g., "Table A, Seat 3")
  assigned_by uuid REFERENCES public.profiles(id) ON DELETE SET NULL,
  created_at timestamptz DEFAULT now()
);

