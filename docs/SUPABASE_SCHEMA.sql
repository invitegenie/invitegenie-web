-- RLS Policies (Examples - customize as needed)

-- Profiles: Users can view all profiles, but only update their own
CREATE POLICY "Allow all authenticated users to view profiles" ON public.profiles FOR SELECT USING (auth.role() = 'authenticated');
CREATE POLICY "Allow users to update their own profile" ON public.profiles FOR UPDATE USING (auth.uid() = id);

-- Events: Authenticated users can read all active events. Hosts can manage their own events.
CREATE POLICY "Allow authenticated users to view active events" ON public.events FOR SELECT USING (status = 'active' AND auth.role() = 'authenticated');
CREATE POLICY "Allow hosts to manage their own events" ON public.events FOR ALL USING (host_id = auth.uid());

-- Tickets: Users can read their own tickets. Event hosts can read tickets for their events.
CREATE POLICY "Allow users to view their own tickets" ON public.tickets FOR SELECT USING (user_id = auth.uid());
CREATE POLICY "Allow event hosts to view tickets for their events" ON public.tickets FOR SELECT USING (EXISTS (SELECT 1 FROM public.events WHERE events.id = tickets.event_id AND events.host_id = auth.uid()));
CREATE POLICY "Allow authenticated users to create tickets" ON public.tickets FOR INSERT WITH CHECK (auth.role() = 'authenticated');

-- Payments: Users can read their own payments.
CREATE POLICY "Allow users to view their own payments" ON public.payments FOR SELECT USING (user_id = auth.uid());
CREATE POLICY "Allow authenticated users to create payments" ON public.payments FOR INSERT WITH CHECK (auth.role() = 'authenticated');

-- Invitations: Users can read invitations sent to them. Hosts can manage invitations for their events.
CREATE POLICY "Allow guests to view their own invitations" ON public.invitations FOR SELECT USING (guest_id = auth.uid() OR guest_email = (SELECT email FROM public.profiles WHERE id = auth.uid()));
CREATE POLICY "Allow hosts to manage invitations for their events" ON public.invitations FOR ALL USING (host_id = auth.uid());

-- Invitation Guests: Hosts can manage guests for their events. Staff can read.
CREATE POLICY "Allow hosts to manage guests for their events" ON public.invitation_guests FOR ALL USING (EXISTS (SELECT 1 FROM public.events WHERE events.id = invitation_guests.event_id AND events.host_id = auth.uid()));
CREATE POLICY "Allow staff to read all invitation guests" ON public.invitation_guests FOR SELECT USING (auth.jwt() ->> 'user_role' = 'staff'); -- Assuming user_role is in JWT claims

-- Invitation Logs: Read-only for hosts of the associated event.
CREATE POLICY "Allow hosts to read invitation logs for their events" ON public.invitation_logs FOR SELECT USING (EXISTS (SELECT 1 FROM public.invitation_guests WHERE invitation_guests.id = invitation_logs.guest_id AND EXISTS (SELECT 1 FROM public.events WHERE events.id = invitation_guests.event_id AND events.host_id = auth.uid())));

-- Checkins: Staff can create checkins. Hosts can read checkins for their events.
CREATE POLICY "Allow staff to create checkins" ON public.checkins FOR INSERT WITH CHECK (auth.jwt() ->> 'user_role' = 'staff');
CREATE POLICY "Allow hosts to read checkins for their events" ON public.checkins FOR SELECT USING (EXISTS (SELECT 1 FROM public.events WHERE events.id = checkins.event_id AND events.host_id = auth.uid()));

-- Memories: All authenticated users can view. Users can create their own.
CREATE POLICY "Allow authenticated users to view memories" ON public.memories FOR SELECT USING (auth.role() = 'authenticated');
CREATE POLICY "Allow users to create their own memories" ON public.memories FOR INSERT WITH CHECK (user_id = auth.uid());

-- Posts: All authenticated users can view. Users can create their own.
CREATE POLICY "Allow authenticated users to view posts" ON public.posts FOR SELECT USING (auth.role() = 'authenticated');
CREATE POLICY "Allow users to create their own posts" ON public.posts FOR INSERT WITH CHECK (user_id = auth.uid());

-- Comments: All authenticated users can view. Users can create their own.
CREATE POLICY "Allow authenticated users to view comments" ON public.comments FOR SELECT USING (auth.role() = 'authenticated');
CREATE POLICY "Allow users to create their own comments" ON public.comments FOR INSERT WITH CHECK (user_id = auth.uid());

-- Galleries: All authenticated users can view. Hosts can upload to their events.
CREATE POLICY "Allow authenticated users to view galleries" ON public.galleries FOR SELECT USING (auth.role() = 'authenticated');
CREATE POLICY "Allow hosts to upload to their event galleries" ON public.galleries FOR INSERT WITH CHECK (EXISTS (SELECT 1 FROM public.events WHERE events.id = galleries.event_id AND events.host_id = auth.uid()));

-- Vendors: All authenticated users can view verified vendors. Vendors can manage their own profile.
CREATE POLICY "Allow authenticated users to view verified vendors" ON public.vendors FOR SELECT USING (is_verified = TRUE AND auth.role() = 'authenticated');
CREATE POLICY "Allow vendors to manage their own profile" ON public.vendors FOR ALL USING (user_id = auth.uid());

-- Freelancers: All authenticated users can view verified freelancers. Freelancers can manage their own profile.
CREATE POLICY "Allow authenticated users to view verified freelancers" ON public.freelancers FOR SELECT USING (is_verified = TRUE AND auth.role() = 'authenticated');
CREATE POLICY "Allow freelancers to manage their own profile" ON public.freelancers FOR ALL USING (user_id = auth.uid());

-- Gigs: All authenticated users can view open gigs. Hosts can manage their own gigs.
CREATE POLICY "Allow authenticated users to view open gigs" ON public.gigs FOR SELECT USING (status = 'open' AND auth.role() = 'authenticated');
CREATE POLICY "Allow hosts to manage their own gigs" ON public.gigs FOR ALL USING (host_id = auth.uid());

-- Freelancer Bookings: Users can view their own bookings. Freelancers can view bookings for their gigs.
CREATE POLICY "Allow users to view their own freelancer bookings" ON public.freelancer_bookings FOR SELECT USING (user_id = auth.uid());
CREATE POLICY "Allow freelancers to view bookings for their gigs" ON public.freelancer_bookings FOR SELECT USING (freelancer_id = auth.uid());

-- Seat Maps: Hosts can manage seat maps for their events.
CREATE POLICY "Allow hosts to manage seat maps for their events" ON public.seat_maps FOR ALL USING (EXISTS (SELECT 1 FROM public.events WHERE events.id = seat_maps.event_id AND events.host_id = auth.uid()));

-- Guest Assignments: Hosts can manage guest assignments for their events.
CREATE POLICY "Allow hosts to manage guest assignments for their events" ON public.guest_assignments FOR ALL USING (EXISTS (SELECT 1 FROM public.events WHERE events.id = guest_assignments.event_id AND events.host_id = auth.uid()));

